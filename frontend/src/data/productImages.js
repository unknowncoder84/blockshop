/**
 * Get appropriate image for a product based on its name
 */
export function getProductImage(productName) {
  const nameLower = productName.toLowerCase();
  
  // Check for exact phrase matches first (more specific)
  for (const [keyword, imageUrl] of Object.entries(PRODUCT_IMAGE_MAP)) {
    if (keyword.includes(' ') && nameLower.includes(keyword)) {
      return imageUrl;
    }
  }
  
  // Then check for single word matches
  for (const [keyword, imageUrl] of Object.entries(PRODUCT_IMAGE_MAP)) {
    if (!keyword.includes(' ') && nameLower.includes(keyword)) {
      return imageUrl;
    }
  }
  
  // Return default if no match
  return PRODUCT_IMAGE_MAP.default;
}
